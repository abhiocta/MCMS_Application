package com.involveininnovation.chat.model;

/**
 * Enumeration representing the status of a chat interaction.
 */
public enum Status {

    /**
     * Indicates a user has joined the chat.
     */
    JOIN,

    /**
     * Indicates a regular chat message.
     */
    MESSAGE,

    /**
     * Indicates a user has left the chat.
     */
    LEAVE
}
